package javaProgrammes;

public class MathMethods {
    public static void main(String[]Args){

        // power
        System.out.println(Math.pow(5,2));          // For power

        // abs
        System.out.println(Math.abs(-25.4));        // To get absolute value
        System.out.println(Math.abs(25.4));

        // ceil
        System.out.println(Math.ceil(25.4));        // To round the value to higher number

        // floor
        System.out.println(Math.floor(25.4));       // To round the value to the lower number

        // max
        System.out.println(Math.max(25,14));        // To get he maximum of two numbers

        // min
        System.out.println(Math.min(25,14));        // To get the minimum of two numbers

        // sqrt
        System.out.println(Math.sqrt(25));          // To get the square root of a number
    }
}

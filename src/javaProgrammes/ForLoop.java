package javaProgrammes;

public class ForLoop {
    public static void main(String[] Args){
        int x;
        for (x=1; x<=5; x++){
            System.out.println("For Loop");
        }

        int i=0;                // This is for comparing with For Loop only
        while (i <= 5){
            i++;
            System.out.println("While Loop");
        }
    }
}
